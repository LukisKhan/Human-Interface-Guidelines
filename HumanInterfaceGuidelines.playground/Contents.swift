Design Themes

Clarity: precise, legible, negative space to highlight content
Deference: fluid motion, content fill entire screen, minimal distractions
Depth: multiple visual layers, relistic motion, touch/discoverability, use transitions

Design Principles

Aesthetic Integrity: appearence and behavior should align with function
Consistency: use well-known elements such as icons, text style, terminology
Direct Manipulation: immediate/visial resutls of direct manipulation
Feedback: highlight, progress indicator, animations, sounds
Metaphors: use familar devices such as toggle switch, sliders, flicking
User Control: user have the ultimate control, not the app
